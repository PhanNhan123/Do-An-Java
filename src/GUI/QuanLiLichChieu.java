    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
       int row = tableQLLC.getSelectedRow();
       LichChieuBUS lcb = new LichChieuBUS();
       if(row == -1)
       {
       JOptionPane.showMessageDialog(null, "Chưa chọn dòng");
       }
       else
       {
       Object []options = {"Đồng Ý", "Hủy"};
       int n = JOptionPane.showOptionDialog(null, "Bạn có chắc chắn muốn xóa dữ liệu không ?", "Xóa", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
       
       if(n == 0){
       baseLC.setNgayChieu(tableQLLC.getValueAt(row, 0).toString());
       baseLC.setBuoiChieu(tableQLLC.getValueAt(row, 1).toString());
       baseLC.setMaPhong(tableQLLC.getValueAt(row, 2).toString());
       baseLC.setMaPhim(tableQLLC.getValueAt(row, 3).toString());
       baseLC.setMaLV(tableQLLC.getValueAt(row, 4).toString());
       if(lcb.DeleteData(baseLC) == true)
       {
              JOptionPane.showMessageDialog(null, "Xóa thành công");
              this.loadDataTable();
       }
       else
       {
              JOptionPane.showMessageDialog(null, "Xóa không thành công");
       
       }
       }
       }
       
       
    }//GEN-LAST:event_buttonDeleteActionPerformed

