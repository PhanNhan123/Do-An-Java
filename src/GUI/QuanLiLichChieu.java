//load data từ database lên bảng
public void loadDataTable()
{
       tableQLLC.removeAll();
       DefaultTableModel model =  (DefaultTableModel)tableQLLC.getModel();
       dateSearch.setCalendar(Calendar.getInstance()); //Sét ngày trong Jdatechooser = ngày hiện tại.
       String []colsName = {"Ngày Chiếu", "Buổi Chiếu", "Mã Phòng","Mã Phim", "Mã Loại Vé"};
       model.setColumnIdentifiers(colsName);
       tableQLLC.setModel(model); 
       model.setRowCount(0);
       dslc = new LichChieuBUS().getDanhSachLichChieu();
        for(LichChieuDTO lct :dslc)
        {
            
            String row[];
            row = new String[5];
            row[0] = lct.getNgayChieu();
            row[1] = lct.getBuoiChieu();
            row[2] = lct.getMaPhong();
            row[3] = lct.getMaPhim();
            row[4] = lct.getMaLV();
           
            model.addRow(row);
        }
        
}

