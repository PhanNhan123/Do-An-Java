{
   tableQLLC.removeAll();
   DefaultTableModel model =  (DefaultTableModel)tableQLLC.getModel();
   
   String []colsName = {"Ngày Chiếu", "Buổi Chiếu", "Mã Phòng","Mã Phim", "Mã Loại Vé"};
       model.setColumnIdentifiers(colsName);
       tableQLLC.setModel(model); 
       model.setRowCount(0);
       dslc = new LichChieuBUS().searchDanhSachLichChieu(ngayChieu, buoiChieu);
        for(LichChieuDTO lct :dslc)
        {
            
            String row[];
            row = new String[5];
            row[0] = lct.getNgayChieu();
            row[1] = lct.getBuoiChieu();
            row[2] = lct.getMaPhong();
            row[3] = lct.getMaPhim();
            row[4] = lct.getMaLV();
           
            model.addRow(row);
        }
        
}



